package pl.jitsolutions.training.javasetests;

import org.junit.Test;

import static org.junit.Assert.assertEquals;

/**
 * Working with abstract classes.
 */
public class Exercise5Test {
    
    @Test
    public void formatText() {
        //GIVEN
        TextFormatter textFormatter = new TextFormatter();

        //WHEN
        String result = textFormatter.formatText("MyFile");

        //THEN
        assertEquals("MyFile.java", result);
    }

    @Test
    public void overrideMethod() { //HINT: anonymous class
        //GIVEN
        TextFormatter textFormatter = new TextFormatter() {
            @Override
            public String formatText(String text) {
                return text;
            }
        };

        //WHEN
        String result = textFormatter.formatText("MyNewFile");

        //THEN
        assertEquals("MyNewFile", result);
    }

    @Test
    public void overloadMethod() {
        //GIVEN
        TextFormatter textFormatter = new TextFormatter();

        //WHEN
        String result = textFormatter.formatNumber(123);

        //THEN
        assertEquals("123.java", result);
    }
}
