package pl.jitsolutions.training.javasetests;

import org.junit.Assert;
import org.junit.Test;

import static org.junit.Assert.assertTrue;

/**
 * Working with interfaces and polymorphism.
 */
public class Exercise8Test {

    @Test
    public void checkStructure() {
        //GIVEN
        Cat cat = new Cat();
        Dog dog = new Dog();
        HuskyDog huskyDog = new HuskyDog();

        //WHEN

        //THEN
        assertTrue(cat instanceof Animal);
        assertTrue(dog instanceof Animal);
        assertTrue(huskyDog instanceof Animal);
        assertTrue(huskyDog instanceof Dog);
    }

    @Test
    public void checkPolymorphism() {
        //GIVEN
        Animal cat = new Cat();
        Animal dog = new Dog();
        Dog huskyDog = new HuskyDog();

        //WHEN
        String catSpeak = cat.speak();
        String dogSpeak = dog.speak();
        String huskyDogSpeak = huskyDog.speak();

        //THEN
        Assert.assertEquals("miau miau...", catSpeak);
        Assert.assertEquals("hau hau...", dogSpeak);
        Assert.assertEquals("", huskyDogSpeak);

    }
}
