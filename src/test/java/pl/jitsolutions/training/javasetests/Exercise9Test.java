package pl.jitsolutions.training.javasetests;

import org.junit.Test;

import static org.junit.Assert.assertEquals;

/**
 * Working with static and final key words.
 */
public class Exercise9Test {

    @Test
    public void staticMethod() {
        //GIVEN
        String text = "myt";

        //WHEN
        String result = StringFormatter.reverse(text);

        //THEN
        assertEquals("tym", result);
    }

    @Test
    public void constant() {
        //GIVEN

        //WHEN
        String space = StringFormatter.SPACE;

        //THEN
        assertEquals(" ", space);
    }
}
